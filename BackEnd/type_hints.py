### Type Hints ###

my_string_variable = "My string variable"
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 9
print(my_string_variable)
print(type(my_string_variable))

my_string_variable2: str = "My string variable"
print(my_string_variable2)
print(type(my_string_variable2))

my_string_variable2 = 5
print(type(my_string_variable2))



